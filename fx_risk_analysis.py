def ensure_dir(path: str) -> None:
    os.makedirs(path, exist_ok=True)


def load_returns(path: str) -> pd.DataFrame:
    """
    Charge fx_returns.csv au format de votre projet :
    - sep=";"
    - decimal=","
    - index_col=0 = Date
    - valeurs = log returns

    On nettoie :
    - drop colonnes trop vides
    - drop lignes complètement vides
    """
    df = pd.read_csv(path, sep=";", decimal=",", index_col=0, parse_dates=True).sort_index()
    df = df.dropna(axis=1, thresh=200)
    df = df.dropna(how="all")
    return df


def load_weights(path: str, columns: list[str], index: pd.DatetimeIndex) -> pd.DataFrame | None:
    """
    Charge weights.csv si présent.
    Exigences :
    - mêmes colonnes que returns (ou sous-ensemble)
    - mêmes dates (ou dates moins fréquentes -> forward-fill)

    Si absent -> None.
    """
    if not os.path.exists(path):
        return None

    w = pd.read_csv(path, sep=";", decimal=",", index_col=0, parse_dates=True).sort_index()

    # aligne colonnes / dates
    w = w.reindex(columns=columns)
    w = w.reindex(index=index).ffill()

    # remplace NaN par 0 (poids absent)
    w = w.fillna(0.0)

    # renormalise pour que sum(w)=1 chaque jour (si somme=0 -> tout 0)
    s = w.sum(axis=1).replace(0.0, np.nan)
    w = w.div(s, axis=0).fillna(0.0)

    return w


def make_equal_weights(columns: list[str], index: pd.DatetimeIndex) -> pd.DataFrame:
    """Poids égal-pondérés (fallback)"""
    n = len(columns)
    return pd.DataFrame(1.0 / n, index=index, columns=columns)


def log_to_simple(r_log: pd.Series) -> pd.Series:
    """Convertit log-return -> simple return : exp(r)-1"""
    return np.exp(r_log) - 1.0



# 1) VAR HISTORIQUE + ES (ROULANTS)

def rolling_hist_var_es(r: pd.Series, window: int, alpha: float) -> pd.DataFrame:
    """
    Calcule VaR historique et ES (Expected Shortfall) sur fenêtre roulante.

    Convention :
    - VaR et ES sont des nombres POSITIFS représentant une perte.
    - VaR = -quantile(r, 1-alpha)
    - ES  = -moyenne(r | r <= quantile)

    r = série de rendements (ici log-returns ou simple returns, mais rester cohérent)
    """
    out = pd.DataFrame(index=r.index, columns=["VaR_hist", "ES_hist"], dtype=float)

    vals = r.values
    for i in range(len(r)):
        if i + 1 < window:
            continue

        w = vals[i + 1 - window : i + 1]
        w = w[~np.isnan(w)]
        if len(w) < max(30, window // 3):
            continue

        q = np.quantile(w, 1.0 - alpha)   # quantile de la queue gauche
        var = -q                          # perte positive
        tail = w[w <= q]
        es = -tail.mean() if len(tail) > 0 else np.nan

        out.iloc[i, 0] = var
        out.iloc[i, 1] = es

    return out


# 2) BACKTESTING VAR (BREACHES)

def var_breaches(r: pd.Series, var_series: pd.Series) -> pd.Series:
    """
    Un breach VaR se produit si :
      r < -VaR
    car VaR est une perte positive.

    Retourne une série 1/0 (NaN si VaR indisponible).
    """
    breach = (r < -var_series).astype(float)
    breach[var_series.isna()] = np.nan
    breach.name = "VaR_breach"
    return breach


def summarize_breaches(breach: pd.Series, alpha: float) -> dict:
    """Résumé du backtest VaR (taux observé vs attendu)"""
    b = breach.dropna()
    if len(b) == 0:
        return {
            "n_obs": 0,
            "n_breaches": 0,
            "breach_rate": np.nan,
            "expected_rate": 1.0 - alpha,
        }

    n = len(b)
    nb = int(b.sum())
    return {
        "n_obs": int(n),
        "n_breaches": nb,
        "breach_rate": float(nb / n),
        "expected_rate": float(1.0 - alpha),
    }


# 3) DRAWDOWN + RISK CONTROLLER

def equity_from_log_returns(r_log: pd.Series) -> pd.Series:
    """Equity curve base 1 à partir de log returns"""
    return np.exp(r_log.cumsum())


def drawdown_from_equity(equity: pd.Series) -> pd.Series:
    """Drawdown (négatif ou 0)"""
    peak = equity.cummax()
    return (equity - peak) / peak


def exposure_from_drawdown(dd_mag: float, tiers: list[tuple[float, float]]) -> float:
    """
    dd_mag = drawdown en valeur positive (ex: 0.12 pour -12%)
    tiers: liste (seuil_dd, expo)

    On prend le dernier palier dont seuil <= dd_mag.
    """
    expo = tiers[0][1]
    for thr, m in tiers:
        if dd_mag >= thr:
            expo = m
    return float(expo)


def build_risk_controller(
    port_log_returns_net: pd.Series,
    tiers: list[tuple[float, float]],
    daily_cut: float,
    daily_flat: float,
) -> pd.DataFrame:
    """
    Construit une "gouvernance" de risque qui sort un multiplicateur d'exposition quotidien.

    - Deleveraging par drawdown (expo_dd)
    - Circuit breaker par perte journalière (expo_cb)
    - expo_final = min(expo_dd, expo_cb)

    IMPORTANT : on applique les seuils daily_cut/daily_flat en SIMPLE RETURNS.
    """
    equity = equity_from_log_returns(port_log_returns_net)
    dd = drawdown_from_equity(equity)              # négatif
    dd_mag = (-dd).clip(lower=0.0)                 # positif

    expo_dd = dd_mag.apply(lambda x: exposure_from_drawdown(float(x), tiers))
    expo_dd.name = "exposure_dd"

    # circuit breaker basé sur simple returns
    simple_r = log_to_simple(port_log_returns_net)
    expo_cb = pd.Series(1.0, index=simple_r.index, name="exposure_cb")
    expo_cb[simple_r <= daily_flat] = 0.0
    expo_cb[(simple_r <= daily_cut) & (simple_r > daily_flat)] = 0.5

    expo_final = pd.Series(
        np.minimum(expo_dd.values, expo_cb.values),
        index=simple_r.index,
        name="exposure_final",
    )

    return pd.DataFrame(
        {
            "equity": equity,
            "drawdown": dd,
            "drawdown_mag": dd_mag,
            "exposure_dd": expo_dd,
            "exposure_cb": expo_cb,
            "exposure_final": expo_final,
        }
    )



# 4) CORRELATION STRESS (SUR RETURNS)

def avg_abs_offdiag_corr(df: pd.DataFrame) -> float:
    """
    Moyenne des corrélations absolues hors diagonale.
    """
    corr = df.corr()
    if corr.shape[0] < 2:
        return np.nan
    m = corr.values
    n = m.shape[0]
    mask = ~np.eye(n, dtype=bool)
    return float(np.nanmean(np.abs(m[mask])))


def rolling_corr_stress(
    returns_df: pd.DataFrame, window: int, threshold: float
) -> pd.DataFrame:
    """
    Calcule :
    - corr_abs_mean (moyenne abs corr off-diagonal)
    - corr_stress_flag = 1 si > threshold
    """
    out = pd.DataFrame(index=returns_df.index, columns=["corr_abs_mean", "corr_stress_flag"], dtype=float)

    for i in range(len(returns_df)):
        if i + 1 < window:
            continue

        w = returns_df.iloc[i + 1 - window : i + 1]

        # garde uniquement colonnes suffisamment présentes
        w = w.dropna(axis=1, thresh=window // 2)

        # impose dates communes dans la fenêtre
        w = w.dropna(how="any")

        if w.shape[0] < max(30, window // 3) or w.shape[1] < 2:
            continue

        c = avg_abs_offdiag_corr(w)
        out.iloc[i, 0] = c
        out.iloc[i, 1] = 1.0 if (not np.isnan(c) and c > threshold) else 0.0

    return out



# 5) TURNOVER + COUTS

def turnover(weights_df: pd.DataFrame) -> pd.Series:
    """
    Turnover journalier standard :
      turnover_t = sum(|w_t - w_{t-1}|) / 2

    /2 permet d'interpréter comme "fraction de portefeuille tradée".
    """
    t = weights_df.diff().abs().sum(axis=1) / 2.0
    t.name = "turnover"
    return t


def apply_costs(
    port_log_returns_gross: pd.Series,
    turnover_series: pd.Series,
    cost_bps_per_turnover: float,
) -> pd.DataFrame:
    """
    Modèle ultra-simple :
    coût_t = turnover_t * (bps / 10000)
    r_net = r_gross - coût_t

    Retourne :
    - cost
    - port_log_returns_net
    """
    cost = turnover_series * (cost_bps_per_turnover / 10000.0)
    cost.name = "cost"
    r_net = (port_log_returns_gross - cost.fillna(0.0)).rename("port_log_returns_net")
    return pd.concat([cost, r_net], axis=1)



# 6) PORTEFEUILLE : RETURNS A PARTIR DE POIDS

def portfolio_log_returns(returns_df: pd.DataFrame, weights_df: pd.DataFrame) -> pd.Series:
    """
    Rendement log du portefeuille : somme(w * r) (approximation cohérente pour log returns)
    """
    aligned_r = returns_df.reindex(index=weights_df.index, columns=weights_df.columns)
    pr = (aligned_r * weights_df).sum(axis=1)
    pr.name = "port_log_returns_gross"
    return pr


# 7) STRESS TESTS (SIMPLES)

def stress_tests(
    port_simple_returns: pd.Series,
    n_worst: int,
    shock_sigmas: list[int],
) -> pd.DataFrame:
    """
    Stress tests très simples :
    A) "Worst days" : moyenne des n pires jours
    B) Chocs paramétriques : -k * sigma (sur simple returns)

    Retourne un tableau de pertes (positives).
    """
    r = port_simple_returns.dropna()
    if len(r) < 30:
        return pd.DataFrame({"stress_loss": [np.nan]}, index=["insufficient_data"])

    # A) worst days
    worst = r.nsmallest(min(n_worst, len(r)))
    worst_mean = -worst.mean()  # perte positive

    # B) shocks
    sigma = r.std()
    shocks = {f"shock_-{k}sigma": float(k * sigma) for k in shock_sigmas}

    rows = {
        "worst_days_mean_loss": float(worst_mean),
        **shocks,
    }
    return pd.DataFrame.from_dict(rows, orient="index", columns=["stress_loss"])




