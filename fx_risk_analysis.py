# MAIN PIPELINE RISQUE

def main(cfg: dict) -> None:
    ensure_dir(cfg["out_dir"])

    # 1) Charge returns
    returns_df = load_returns(cfg["returns_path"])
    # Important pour portefeuille : dates communes (sinon pondération/risque compliqué)
    returns_df = returns_df.dropna()

    # 2) Charge weights (ou égal-pondéré)
    w = load_weights(cfg["weights_path"], columns=list(returns_df.columns), index=returns_df.index)
    if w is None:
        w = make_equal_weights(columns=list(returns_df.columns), index=returns_df.index)

    # 3) Rendements portefeuille BRUTS
    port_gross = portfolio_log_returns(returns_df, w)

    # 4) Turnover + coûts => rendements NETS
    to = turnover(w)
    costs_and_net = apply_costs(port_gross, to, cfg["cost_bps_per_turnover"])
    port_net = costs_and_net["port_log_returns_net"]

    # 5) VaR/ES roulants (sur NET, plus réaliste)
    var_es = rolling_hist_var_es(port_net, window=cfg["var_window"], alpha=cfg["alpha"])

    # 6) Breaches VaR (backtest)
    breaches = var_breaches(port_net, var_es["VaR_hist"])

    # 7) Risk controller (drawdown + circuit breakers)
    controller = build_risk_controller(
        port_log_returns_net=port_net,
        tiers=cfg["dd_exposure_tiers"],
        daily_cut=cfg["daily_loss_cut"],
        daily_flat=cfg["daily_loss_flat"],
    )

    # 8) Corr stress (sur returns par paires)
    corr_stress = rolling_corr_stress(
        returns_df,
        window=cfg["corr_window"],
        threshold=cfg["corr_abs_mean_threshold"],
    )

    # 9) Assemble rapport journalier
    daily = pd.concat(
        [
            port_gross,
            costs_and_net["port_log_returns_net"],
            costs_and_net["cost"],
            to,
            var_es,
            breaches,
            controller,
            corr_stress,
        ],
        axis=1,
    )

    daily_path = os.path.join(cfg["out_dir"], "risk_report_daily.csv")
    daily.to_csv(daily_path, sep=";", decimal=",")
    print(f"[OK] {daily_path}")

    # 10) Résumé (breaches + stress tests + stats simples)
    breach_summary = summarize_breaches(daily["VaR_breach"], cfg["alpha"])

    # stress tests sur rendements SIMPLE nets
    port_net_simple = log_to_simple(port_net)
    stress = stress_tests(
        port_simple_returns=port_net_simple,
        n_worst=cfg["stress_n_worst_days"],
        shock_sigmas=cfg["shock_sigma_levels"],
    )

    # stats additionnelles
    max_dd = float(daily["drawdown"].min()) if "drawdown" in daily else np.nan
    corr_avg = float(np.nanmean(daily["corr_abs_mean"].values))
    corr_stress_days = int(np.nansum(daily["corr_stress_flag"].values))
    exposure_avg = float(np.nanmean(daily["exposure_final"].values))
    stop_days = int(np.nansum((daily["exposure_final"] == 0.0).astype(float).values))
    cost_total = float(np.nansum(daily["cost"].values))
    turnover_avg = float(np.nanmean(daily["turnover"].values))

    summary_rows = {
        "max_drawdown (negative)": max_dd,
        "corr_abs_mean_avg": corr_avg,
        "corr_stress_days": corr_stress_days,
        "exposure_avg": exposure_avg,
        "stop_trading_days": stop_days,
        "cost_total": cost_total,
        "turnover_avg": turnover_avg,
        **{f"VaR_backtest_{k}": v for k, v in breach_summary.items()},
    }

    summary_df = pd.DataFrame.from_dict(summary_rows, orient="index", columns=["value"])
    summary_path = os.path.join(cfg["out_dir"], "risk_summary.csv")
    summary_df.to_csv(summary_path, sep=";", decimal=",")
    print(f"[OK] {summary_path}")

    # Ajoute stress tests au même dossier
    stress_path = os.path.join(cfg["out_dir"], "risk_stress_tests.csv")
    stress.to_csv(stress_path, sep=";", decimal=",")
    print(f"[OK] {stress_path}")

    # 11) Plots
    save_plots(cfg["out_dir"], daily)
    print(f"[OK] Plots sauvegardés dans {cfg['out_dir']}")

    # 12) Affichage console rapide
    print("\n=== SUMMARY (preview) ===")
    print(summary_df.round(6))
    print("\n=== STRESS TESTS (preview) ===")
    print(stress.round(6))


if __name__ == "__main__":
    main(CONFIG)